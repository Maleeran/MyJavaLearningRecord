package src.day10_Packages.src.com.example.utils;

public class Calculater {
    public int add(int a, int b) {
        return a + b;
    }
    protected int subtract(int a, int b) {
        return a - b;
    }
    private int multiply(int a, int b) {
        return a * b;
    }
    int divide(int a, int b) {
        return a / b;
    }
}